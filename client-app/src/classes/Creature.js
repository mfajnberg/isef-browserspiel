export class Creature {
    constructor(name, portraitUrl, int, dis, pow, agi, luc, cha, ali, tem, mor) {
        this.Name = name
        this.PortraitUrl = portraitUrl
        this.Intellect = int
        this.Discipline = dis
        this.Power = pow
        this.Agility = agi
        this.Lucidity = luc
        this.Charisma = cha
        this.Alignment = ali
        this.Temperament = tem
        this.Morale = mor
    }
}